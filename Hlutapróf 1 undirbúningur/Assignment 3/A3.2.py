# Write a program using a while statement, that given the number n as input, 
# prints the first n odd numbers starting from 1.

# For example if the input is
# 4

# The output will be:
# 1
# 3
# 5
# 7

n = int(input('Enter a integer: '))

counter = 1
i = 1
while counter <= n:
    print (i)
    i+=2
    counter += 1





